<template>
    <b-row>
        <b-col md="2"></b-col>
        <b-col md="8" id="success_fixed">
            <div class="jumbotron">
                <h2>
                    {{ $t('lang.success.h2') }}
                </h2>
                <p v-html="$t('lang.success.p[0].text', { key: key })"></p>
                <ul>
                    <li><a v-html="$t('lang.success.ul.li[0].text')"></a>&nbsp;<b-badge
                            pill class="badge-fixed"
                            href="#"
                            @mouseenter="popover_show = true"
                            @mouseleave="popover_show = false">?</b-badge>
                    </li>
                    <li>{{ $t('lang.success.ul.li[1].browser') }}
                        <a v-b-tooltip.hover="$t('lang.success.ul.li[1].tooltip')"
                           :href="base_url + ( '000' + key ).substr(-3,3)"
                           target="_blank">
                            {{ base_url + ( "000" + key ).substr(-3,3) }}
                        </a>&nbsp;<b-badge
                                variant="info"
                                class="badge-fixed"
                                :data-clipboard-text="base_url + key"
                                href="#">
                            {{ $t('lang.success.badge.' +
                            (copy_btn_status > 0 ? 'success' : (copy_btn_status === 0 ?  'copy' : 'fail')))  }}
                        </b-badge>
                    </li>
                    <li>
                        <b-link id="qr_code_link">{{ $t('lang.success.ul.li[2].scan_qr_code') }}</b-link>
                    </li>
                </ul>
                <p>
                    <b-button @click.prevent="goHome" variant="primary">{{ $t('lang.success.p[1].button') }}</b-button>
                </p>
            </div>
        </b-col>
        <b-col md="2"></b-col>
        <b-popover
                :show.sync="popover_show"
                target="nav_input"
                placement="bottomright">
            <a v-html="$t('lang.success.popover.text')"></a>
        </b-popover>
        <b-popover
                target="qr_code_link"
                placement="auto"
                triggers="hover">
            <div class="text-center">
                <QRCode :value="this.base_url + this.key" :options="{ width: 168 }"></QRCode>
            </div>
        </b-popover>
    </b-row>
</template>

<script>
    import { mapGetters } from "vuex";
    import stateMixin from "../assets/js/mixins/stateMixin";
    export default {
        name: "Success",
        mixins: [stateMixin],
        data() {
            return {
                base_url: location.origin + '/',
                copy_btn_status: 0,
                popover_show: false,
            }
        },
        computed: {
            ...mapGetters([
                "key"
            ])
        },
        mounted() {
            let clipboard = new this.clipboard('.badge-fixed');
            let cur = this;
            clipboard.on('success', function() {
                cur.copy_btn_status = 1;
                window.setTimeout(function () {
                    cur.copy_btn_status = 0;
                }, 2000);
            });
            clipboard.on('error', function() {
                cur.copy_btn_status = -1;
                window.setTimeout(function () {
                    cur.copy_btn_status = 0;
                }, 2000);
            });
        },
        methods: {
            goHome() {
                if (this.$route.params.key !== '') {
                    this.$router.push('/');
                } else {
                    this.updateView("home");
                }
            }
        }
    }
</script>

<style scoped>
    #success_fixed {
        margin-top: 1.375em;
        margin-bottom: 1.375em;
    }

    .badge-fixed {
        position: relative;
        bottom: .2em;
    }
</style>
